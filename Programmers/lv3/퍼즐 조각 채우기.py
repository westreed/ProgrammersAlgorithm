def returnPoint(Piece):
    leftX, leftY = 0xFFFFFFFF, 0xFFFFFFFF
    for x,y in Piece:
        if x < leftX: leftX = x
        if y < leftY: leftY = y
    return leftX, leftY

def setPoint(Piece):
    Point = returnPoint(Piece)
    for idx in range(len(Piece)):
        Piece[idx] = (Piece[idx][0]-Point[0], Piece[idx][1]-Point[1])

def RotatePoint(Piece):
    for idx in range(len(Piece)):
        x,y = Piece[idx]
        Piece[idx] = (-y,x)
    setPoint(Piece)
    Piece.sort(key= lambda x:(x[0]+x[1]))

def searchPiece(Board, Size, Check):
    from collections import deque

    Piece = []
    for y in range(Size):
        for x in range(Size):
            if Board[y][x] == Check:
                Piece_ = [(x,y)]
                node = deque([(x,y)])
                temp = deque()

                while node:
                    cx,cy = node.popleft()
                    for dx,dy in (-1,0), (1,0), (0,-1), (0,1):
                        nx,ny = cx+dx, cy+dy
                        if 0 <= nx and nx < Size and 0 <= ny and ny < Size and Board[ny][nx] == Check:
                            if (nx,ny) not in Piece_:
                                Piece_.append((nx,ny))
                                temp.append((nx,ny))
                    if not node:
                        if not temp: break
                        node = temp
                        temp = deque()

                for vx,vy in Piece_: Board[vy][vx] = not Check
                Piece_ = sorted(Piece_, key= lambda x:(x[0]+x[1]))
                setPoint(Piece_)
                Piece.append(Piece_)
    return Piece

def solution(game_board, table):
    #보드크기
    Size = len(table)
    table_Piece = searchPiece(table, Size, 1)
    blank_Piece = searchPiece(game_board, Size, 0)

    answer = 0
    idx = 0
    rotate = 0
    while idx < len(table_Piece):
        Skip = False
        Piece = table_Piece[idx]
        for idx2 in range(len(blank_Piece)):
            if Piece == blank_Piece[idx2]:
                answer += len(Piece)
                del blank_Piece[idx2]
                del table_Piece[idx]
                Skip = True
                break

        if Skip is True:
            rotate = 0
            continue

        if rotate < 3:
            rotate += 1
            RotatePoint(Piece)
        else:
            rotate = 0
            idx += 1

    return answer

